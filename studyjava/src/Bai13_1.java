public class Bai13_1 {
    private int ngay;
    private int thang;
    private int nam;

    public Bai13_1(int ngay, int thang, int nam)
    {
        this.ngay = ngay;
        this.nam = nam;
        this.thang = thang;
    }

    public int getNgay() {
        return this.ngay;
    }

    public void setNgay(int ngay) {
        this.ngay = ngay;
    }

    public int getThang() {
        return this.thang;
    }

    public void setThang(int thang) {
        this.thang = thang;
    }

    public int getNam() {
        return this.nam;
    }

    public void setNam(int nam) {
        this.nam = nam;
    }


}
